import java.lang.*;
// lec number : 16
public class numberToBinary {
	public static void main(String[] args) {
		int postiveNum = 4;
		int negativeNum = -4;
		// getting x's value in binary form
		System.out.println(Integer.toBinaryString(postiveNum));
		System.out.println(Integer.toBinaryString(negativeNum));
	}
}
